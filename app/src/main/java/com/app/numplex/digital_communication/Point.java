package com.app.numplex.digital_communication;

// Classe para representar um ponto (símbolo) com coordenadas x e y
class Point {
    public double x;
    public double y;

    // Construtor para criar um ponto com coordenadas x, y e probabilidade do símbolo
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
